package Objects;

/**
 *
 * @author Makin Artavia
 */
public class University {
    private String name;
    private Integer id;

    public University() {
        this.name = null;
        this.id = null;
    }

    public University(String name, Integer id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public Integer getId() {
        return id;
    }
    
    
}
